function Get-M365SATReportJSON
{
	param ($object)
	
	#Add Extra Objects to the JSON list 
	$Tenant = (Get-AcceptedDomain | ? { $_.Default -eq 'True' }).DomainName.Split('.')[0]
	$Result = New-Object -TypeName PSObject -Property @{
		ResultDate = $(Get-Date -format s)
		Tenant	   = $Tenant
		Results    = $object
	}
	$Result | ConvertTo-Json -Depth 100 | Out-File -FilePath $OutputFile
}


